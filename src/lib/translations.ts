export function translateText(text: string, lang: 'ar' | 'en'): string {
  if (!text) return '';
  const trimmed = text.trim();
  if (trimmed.startsWith('{') && trimmed.endsWith('}')) {
    try {
      const parsed = JSON.parse(trimmed);
      if (parsed && typeof parsed === 'object') {
        if (parsed[lang] !== undefined && parsed[lang] !== null) {
          return parsed[lang];
        }
        return parsed['ar'] || parsed['en'] || text;
      }
    } catch (e) {
      // Ignore JSON parse error, fallback to normal translation
    }
  }
  if (lang === 'ar') return text;
  if (arToEnMap[trimmed]) {
    return arToEnMap[trimmed];
  }
  return text;
}
